#include "functions.h"

double sum(double num1, double num2){    //sum
    return num1 + num2;
} 
double diff(double num1, double num2){   // difference
    return num1 - num2;
}
double mult(double num1, double num2){   // multiplication
    return num1 * num2;
}
double div(double num1, double num2){    //division
    // TODO: zero division!
    return num1 / num2;
}

