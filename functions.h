#include <stdio.h>
double sum(double num1, double num2){    // суммирование (summ)
    return num1+num2;
} 
double diff(double num1, double num2){   // вычитание (difference)
    return num1-num2;
}
double mult(double num1, double num2){   // умножение (multiplication)
    return num1*num2;
}
double div(double num1, double num2){    // деление (division)
    return num1/num2;
}